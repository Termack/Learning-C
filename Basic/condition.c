#include <stdio.h>

// Main function 
int main(void) {
    // Declare integer variable
    int num;

    // Assigning value to variable
    num = 5;
    // Establishing a condition
    // and comparing values
    if (num > 10) {
        printf(num," is greater than 10\n");
    } else {
        printf(num," is not greater than 10\n");
    }
    return 0;
}