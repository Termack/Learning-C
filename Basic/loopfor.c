// A simple example of loop using for.
#include <stdio.h>

// Min function
int main(void) {
  // Declare int variable. 
  int i;

  // While variable i has a value 
  // less than 10, add the value 1.
  for (i = 0; i < 10; i++) {
    // Output the current value of "i".
    printf("%d\n",i);
  }
  return 0;
}